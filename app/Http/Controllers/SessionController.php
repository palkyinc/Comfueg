    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        session_start();
        if (isset($_SESSION[$id])){
            unset($_SESSION[$id]);
            return response()->json(true, 200);
        }else {
            return response()->json(false, 200);
        }
    }
